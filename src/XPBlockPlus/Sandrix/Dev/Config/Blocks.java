package XPBlockPlus.Sandrix.Dev.Config;

public class Blocks {
	
	String mat;
	int xp;
	int count;
	
	public Blocks(String mat, int xp, int count){
		
		this.mat = mat;
		this.xp = xp;
		this.count = count;
		
	}
	
	public String getMat() {
		return mat;
	}
	
	public int getXP() {
		return xp;
	}
	
	public int getCount() {
		return count;
	}

}
